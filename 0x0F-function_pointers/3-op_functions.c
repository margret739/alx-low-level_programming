#include "3-calc.h"

/**
 * op_add - calculate the sum of two integers
 * @a: first integer
 * @b: second integer
 * Return: sum of a and b
 */

int op_add(int a, int b)

{
	return (a + b);
}

/**
 * op_sub - returns the difference of two numbers
 * @a: first integer
 * @b: second integer
 * Return: the difference of a and b
 */

int op_sub(int a, int b)

{
	return (a - b);
}

/**
 * op_mul - calculates the multiplication ot two numbers
 * @a: first integer
 * @b: second integer
 * Return: the multiplication of a and b
 */

int op_mul(int a, int b)

{
	return (a * b);
}

/**
 * op_div - calculates the division of two numbers
 * @a: first integer
 * @b: second integer
 * Return: division of a and b
 */

int op_div(int a, int b)

{
	return (a / b);
}

/**
 * op_mod - finds the remainder of division of two numbers
 * @a: first integer
 * @b: second integer
 * Return: module of a and b
 */

int op_mod(int a, int b)

{
	return (a % b);
}
